
package ejercicio3;


public class Calculos {

    public Calculos() {
    }
    
    public long sumar(long num1, long num2){
        return num1 + num2;
    }
    
    public long restar(long num1, long num2){
        return num1 - num2;
    }
    
    public long multiplicar(long num1, long num2){
        return num1 * num2;
    }
    
}
